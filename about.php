            <h2>What Is ELearning?</h2>
            
            <p><b>E-learning, or electronic learning, is education that doesn't require students to attend class at a physical location and is often completed at the student's own pace. Read on to learn more about e-learning options.</b></p>
            <p>E-learning is often referred to as distance or online learning. Colleges and universities usually offer e-learning classes that cover the same topics as classes that are taught on campus. During e-learning programs, students can interact with faculty via e-mail, chat sessions or Web-based applications like course management systems.</p>
            <h2>Important Facts About E-Learning</h2>
            <h3>Degree Levels</h3>
            <p>Associate's, bachelor's, master's and doctorate degrees are available</p>
            <h3>Prerequisites</h3>
            <p>Varies, depending upon intended program of enrollment; associate's and bachelor's degree program usually require a high school diploma, or equivalent</p>
            <h3>Concentrations</h3>
            <p>Psychology, political science, English, sport management, business administration, accounting, public health, marketing</p>
            <h3>Possible Careers</h3>
            <p>Artist, power plant operator, insurance appraiser, engineer, writer, camera operator, financial advisor, budget analyst, construction manager</p>
            <h2>Benefits of E-Learning</h2>
            <p>Students participating in e-learning can work at their own pace. Although assignments may be due at specific times, students don't have to worry about handing in work at each class session. Students who work or have other responsibilities can structure their e-learning coursework around their schedules. Students who are visual learners will have the advantage of accessing coursework that is mostly text-based; however, some electronic courses use video and audio.</p>
            <h2>Disadvantages of E-Learning</h2>
            <p>E-learning may not work for students who are not highly motivated. Instructors may not always be available when help is needed. Older computers and dial-up connections can make e-learning difficult because files take a long time to download. Students must be comfortable with computers and have access to basic software, like Microsoft Word. Lab work is difficult to emulate in a virtual classroom; students enrolled in courses that rely on hands-on training may need to complete portions of their program on campus or at other sites.</p>
